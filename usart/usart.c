/**
 * Send a byte through the usartx.
 * \param pUSARTx: Select the usart or uart.
 * \param byte: The byte to send.
 * \retval None
 */
void usart_send_byte(USART_TypeDef * pUSARTx, uint8_t byte)
{

	USART_SendData(pUSARTx, byte);

	while (USART_GetFlagStatus(pUSARTx, USART_FLAG_TXE) == RESET)
		;
}

void usart_send_string(USART_TypeDef * pUSARTx, char* string)
{
	u8 i = 0;
	for (i = 0; i < strlen(string); i++)
	{
		usart_send_byte(pUSARTx, (u8) string[i]);

	}
}
void usart_send_array(USART_TypeDef * pUSARTx, u8* array, u8 length)
{
	u8 i = 0;
	for (i = 0; i < length; i++)
	{
		usart_send_byte(pUSARTx, array[i]);

	}
}

